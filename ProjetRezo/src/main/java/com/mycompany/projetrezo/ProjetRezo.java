/**
 * 
 * @param evt 
 * La classe suivante permet de lister toutes les adresses possible grace au while en tapant une adresse et un masque de sous-réseau.
 */
    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
DefaultListModel liste = (DefaultListModel)jList1.getModel();
liste.clear();
int a = 0;
int b = 0;
int c = 0;
switch(result) {
    case 1: {
        while (a < 256){
            ++a;
            while (b < 256){
                ++b;
                while (c < 256){
                    ++c;
          
            liste.addElement(jTextField1.getText() + "." + a + "." + b + "." + c);
            }
        }
    }
liste.remove(0);
liste.remove(16777214);
break;
    }
    case 2: {
        while (a < 256){
            ++a;
            while (b < 256){
                ++b;
                liste.addElement(jTextField1.getText() + "." + jTextField4.getText() + "." + b + "." + c);
            }
        }
liste.remove(0);
liste.remove(65534);
break; 
    }
    case 3: {
        while (a < 256){
            ++a;
            liste.addElement(jTextField1.getText() + "." + jTextField4.getText() + "." + jTextField6.getText() + "." + c);
        }
        liste.remove(0);
        liste.remove(254);
        break;
    }
    }
    }//GEN-LAST:event_jButton6ActionPerformed

