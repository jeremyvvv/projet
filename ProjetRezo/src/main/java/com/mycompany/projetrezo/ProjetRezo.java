/**
 * 
 * @param evt
 * La classe suivante permet de tester si les deux adresses IP sont dans le même réseau et peuvent communiquer en fonction de leur masque.
 */
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        ip1 = Integer.parseInt(jTextField1.getText());
        ip2 = Integer.parseInt(jTextField3.getText());
        ip3 = Integer.parseInt(jTextField4.getText());
        ip4 = Integer.parseInt(jTextField7.getText());
        ip5 = Integer.parseInt(jTextField6.getText());
        ip6 = Integer.parseInt(jTextField8.getText());
        masque1 = jTextField2.getText();

        if (ip == 1){
            if (masque1.equals("255.0.0.0") && ip1 == ip2) {
                JOptionPane.showMessageDialog(this, "Ping réussi");
            }
            else {
                JOptionPane.showMessageDialog(this, "Erreur");
            }
        }

        else if (ip == 2){
            if (masque1.equals("255.255.0.0") && ip1 == ip2 && ip3 == ip4) {
                JOptionPane.showMessageDialog(this, "Ping réussi");
            }
            else {
                JOptionPane.showMessageDialog(this, "Erreur");
            }
        }
        else if (ip == 3){
            if (masque1.equals("255.255.255.0") && ip1 == ip2 && ip3 == ip4 && ip5 == ip6) {
                JOptionPane.showMessageDialog(this, "Ping réussi");
            }
            else {
                JOptionPane.showMessageDialog(this, "Erreur");
            }
        }

        else {
            JOptionPane.showMessageDialog(this, "Veuillez saisir une adresse IP valide.");
        }      // TODO add your handling code here:
    }//GEN-LAST:event_jButton3ActionPerformed
